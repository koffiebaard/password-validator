<h2>Uitleg van controles en wachtwoord sterktes</h2>
<p>De sterkte van een wachtwoord wordt bepaald door de vatbaarheid van de methoden om een wachtwoord te achterhalen. Dat kan direct communicerend met software zijn, of als een bulk wachtwoorden op straat ligt en iemand de encryptie eraf wilt halen. </p><p>Hoe sterk een encryptie is hangt af van waar je een wachtwoord invoert; erg bewerkelijk dus. Vandaar deze tool, ondersteunend in het kiezen van sterke wachtwoorden, ongeacht de opslagmethode.</p>
<ul id="uitleg">
<li>
	<h3>Lengte van het wachtwoord</h3>
	<p>De lengte van een wachtwoord is erg belangrijk. Elk karakter wat een wachtwoord extra heeft, zal het exponentieel moeilijker maken om het wachtwoord te achterhalen.</p>
</li>
<li>
	<h3>Geen woorden of namen</h3>
	<p>Software dat is ontworpen om wachtwoorden te achterhalen, gebruikt ook het woordenboek, en een lijst met namen en termen. Evenals variaties daarop, zoals klinkers vervangen met cijfers en er 1 of 2 letters of cijfers achterplakken.</p>
</li>
<li>
	<h3>Diversiteit in karakters</h3>
	<p>Het achterhalen van wachtwoorden is grotendeels berust op assumpties. Bijvoorbeeld, als iemand er vanuit gaat dat je enkel letters gebruikt (met hoofdletters) tussen de 1 tot 8 karakters, en dat klopt, dan is je wachtwoord erg snel gevonden (secondenwerk).</p><p>Zo gaat dat veel verder, tot combinaties met cijfers en andere tekens, maar de kern blijft: Assumpties. Val dus niet in de 'mainstream' categorieen en gebruik diverse karakters, zoals spaties en en uitroeptekens.</p>
</li>
<li>
	<h3>Hoe onthoud je een goed wachtwoord dan?</h3>
	<p>Een van de grotere problemen bij het kiezen van een wachtwoord. Keuze zat als het om veilige wachtwoorden gaat. Maar hoe onthoud je ze? Daar komt deze tool mooi bij kijken: Tik verschillende wachtwoorden in, kijk hoe je de onthoudbaarheid vindt en de tool helpt je met de puntjes en i-tjes in termen van veiligheid.</p>
</li>
